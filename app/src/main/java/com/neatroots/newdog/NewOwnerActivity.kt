// Adapter ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö RecyclerView
class OwnerTipAdapter(private val tips: List<OwnerTip>) :
    androidx.recyclerview.widget.RecyclerView.Adapter<OwnerTipAdapter.ViewHolder>() {

    class ViewHolder(val binding: ItemOwnerTipBinding) :
        androidx.recyclerview.widget.RecyclerView.ViewHolder(binding.root)

    override fun onCreateViewHolder(parent: android.view.ViewGroup, viewType: Int): ViewHolder {
        val binding = ItemOwnerTipBinding.inflate(android.view.LayoutInflater.from(parent.context), parent, false)
        return ViewHolder(binding)
    }

    override fun onBindViewHolder(holder: ViewHolder, position: Int) {
        val tip = tips[position]
        with(holder.binding) {
            emojiText.text = tip.emoji
            titleText.text = tip.title
            detailsText.text = tip.details.joinToString("\n‚Ä¢ ", "‚Ä¢ ")
        }
    }

    override fun getItemCount(): Int = tips.size
}